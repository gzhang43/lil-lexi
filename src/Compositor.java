/**
 * This class establishes the compositor of the document.
 */
public abstract class Compositor {
	
	/**
	 * Sets the composition of the document.
	 */
	public abstract void setComposition(Composition composition);
	
	/**
	 * Composes the structure of the document.
	 */
	public abstract void compose();	
}
